/**
 * Enum representing different character types with varying starting stats.
 *
 * @author Refat
 * @version 1.0
 */
public enum CharacterType {
    /** Business leader with high money, moderate popularity */
    BUSINESS_TYCOON,

    /** Experienced politician with high popularity and influence */
    CAREER_POLITICIAN,

    /** Political newcomer with balanced but lower stats */
    OUTSIDER,

    /** Criminal leader with high money and scandal risk */
    MAFIA_LEADER
}